    private void label_settings_menuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_settings_menuMouseClicked
        // TODO add your handling code here:

        tab_container.setEnabledAt(7, false);
        tab_container.setSelectedIndex(7);

colorObject.hexToRGB();
Color tableColor = new Color(colorObject.r1,colorObject.g1,colorObject.b1);
        // storedUserLabel.setVisible(false);
          jTable1.setVisible(true);
        jTable1.setBackground(tableColor);
        jTable1.setOpaque(false);
               
       try {
            connection = DriverManager.getConnection(databaseUrl);
            Statement st = connection.createStatement();
            String sql = "Select LawyerId,LFName,LLName,LEmail,LPhone,LDate,LGender From Advocate";

            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                String SID = rs.getString("LawyerId");
                String FirstName = rs.getString("LFName");
                String LastName = rs.getString("LLName");
                String Email = rs.getString("LEmail");
                String phoneNumber = rs.getString("LPhone");
                String BirthDate = rs.getString("LDate");
                String Gender = rs.getString("LGender");
                String tbData[] = {SID, FirstName, LastName, Email, phoneNumber, BirthDate, Gender};
                DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
                tblModel.addRow(tbData);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_label_settings_menuMouseClicked

